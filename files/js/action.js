// Functions ball - Page 02
var ball_1 = document.getElementById('ball').innerHTML  = 'b..';
var ball_2 = document.getElementById('small ball').innerHTML  = 'b..';
$('#ball').css('background-color', '#FDD017');
$('#small ball').css('background-color', '#FDD017');
var ball = ball_1 + ball_2;
function ballFn(id) {
    if (ball == 1) {
        document.getElementById("ball").innerHTML = "b.. ";
        $('#ball').css('background-color', '#FDD017');
        document.getElementById("small ball").innerHTML = "b.. ";
        $('#small ball').css('background-color', '#FDD017');
        ball = 0;
    }
    else {
        document.getElementById("ball").innerHTML = "ball.";
        $('#ball').css('background-color', 'white');
        document.getElementById("small ball").innerHTML = "ball.";
        $('#small ball').css('background-color', 'white');
        ball = 1;
    }
}

